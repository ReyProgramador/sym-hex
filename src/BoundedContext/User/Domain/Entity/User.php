<?php

// namespace App\Entity;

// namespace App\Domain\Entity;

namespace App\BoundedContext\User\Domain\Entity;

use App\Repository\UserRepository;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: UserRepository::class)]
class User
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column(length: 255)]
    private ?string $first_name = null;

    #[ORM\Column(length: 255)]
    private ?string $last_name = null;

    #[ORM\Column(length: 255)]
    private ?string $email = null;

    #[ORM\Column(length: 255)]
    private ?string $password = null;

    #[ORM\Column]
    private ?bool $is_buyer = null;

    #[ORM\ManyToOne(inversedBy: 'user_id')]
    private ?UserProduct $user_id = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getFirstName(): ?string
    {
        return $this->first_name;
    }

    public function setFirstName(string $first_name): static
    {
        $this->first_name = $first_name;

        return $this;
    }

    public function getLastName(): ?string
    {
        return $this->last_name;
    }

    public function setLastName(string $last_name): static
    {
        $this->last_name = $last_name;

        return $this;
    }

    public function getEmail(): ?string
    {
        return $this->email;
    }

    public function setEmail(string $email): static
    {
        $this->email = $email;

        return $this;
    }

    public function getPassword(): ?string
    {
        return $this->password;
    }

    public function setPassword(string $password): static
    {
        $this->password = $password;

        return $this;
    }

    public function isIsBuyer(): ?bool
    {
        return $this->is_buyer;
    }

    public function setIsBuyer(bool $is_buyer): static
    {
        $this->is_buyer = $is_buyer;

        return $this;
    }

    public function getUserId(): ?UserProduct
    {
        return $this->user_id;
    }

    public function setUserId(?UserProduct $user_id): static
    {
        $this->user_id = $user_id;

        return $this;
    }
}
